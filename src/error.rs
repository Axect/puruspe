//! This module provides implementations of the error function and related functions.
//!
//! It includes the following main functions:
//! - `erf`: Calculates the error function.
//! - `erfc`: Calculates the complementary error function.
//! - `erfcx`: Calculates the scaled complementary error function.
//! - `inverf`: Calculates the inverse of the error function.
//! - `inverfc`: Calculates the inverse of the complementary error function.

use core::f64::consts::FRAC_2_SQRT_PI;

use crate::{utils::frexp, INV_SQRT_PI};

/// Calculates the error function.
///
/// The error function is defined as:
///
/// $$ \text{erf}(x) = \frac{2}{\sqrt{\pi}} \int_0^x e^{-t^2} dt $$
///
/// # Arguments
///
/// * `x` - The input value
///
/// # Returns
///
/// The value of the error function at `x`
pub fn erf(x: f64) -> f64 {
    if x >= 0f64 {
        1.0 - erfccheb(x)
    } else {
        erfccheb(-x) - 1f64
    }
}

/// Calculates the complementary error function.
///
/// The complementary error function is defined as:
///
/// $$ \text{erfc}(x) = 1 - \text{erf}(x) = \frac{2}{\sqrt{\pi}} \int_x^\infty e^{-t^2} dt $$
///
/// # Arguments
///
/// * `x` - The input value
///
/// # Returns
///
/// The value of the complementary error function at `x`
pub fn erfc(x: f64) -> f64 {
    if x >= 0f64 {
        erfccheb(x)
    } else {
        2f64 - erfccheb(-x)
    }
}

/// Calculates the inverse of the complementary error function.
///
/// This function finds x such that:
///
/// $$ p = \text{erfc}(x) = 1 - \text{erf}(x) = \frac{2}{\sqrt{\pi}} \int_x^\infty e^{-t^2} dt $$
///
/// # Arguments
///
/// * `p` - The probability value (between 0 and 2)
///
/// # Returns
///
/// The value of $x$ for which $\text{erfc}(x) = p$
pub fn inverfc(p: f64) -> f64 {
    // Return arbitrary large pos or neg value
    if p >= 2f64 {
        return -100f64;
    } else if p <= 0f64 {
        return 100f64;
    }

    let pp = if p < 1f64 { p } else { 2f64 - p };
    let t = (-2f64 * (pp / 2f64).ln()).sqrt();
    let mut x = -std::f64::consts::FRAC_1_SQRT_2
        * ((2.30753 + t * 0.27061) / (1f64 + t * (0.99229 + t * 0.04481)) - t);
    for _j in 0..2 {
        let err = erfc(x) - pp;
        x += err / (std::f64::consts::FRAC_2_SQRT_PI * (-x.powi(2)).exp() - x * err);
    }
    if p < 1f64 {
        x
    } else {
        -x
    }
}

/// Calculates the inverse of the error function.
///
/// This function finds x such that:
///
/// $$ p = \text{erf}(x) = \frac{2}{\sqrt{\pi}} \int_0^x e^{-t^2} dt $$
///
/// # Arguments
///
/// * `p` - The probability value (between -1 and 1)
///
/// # Returns
///
/// The value of $x$ for which $\text{erf}(x) = p$
pub fn inverf(p: f64) -> f64 {
    inverfc(1f64 - p)
}

/// Helper function for erfcx(x).
fn cheb_interpolant(x: f64) -> f64 {
    // Application-specific constants:
    const M: i32 = 6; // 2^M subranges
    let j0 = -2; // first octave runs from 2^(j0-1) to 2^j0
    let l0 = 0; // index of x_min in full first octave
    let loff = (j0 + 1) * (1 << M) + l0; // precomputed offset

    // For given x, obtain mantissa xm and exponent je:
    let (xm, je) = frexp(x); // sets xm and je

    // Integer arithmetics to obtain reduced coordinate t:
    let ip = ((1 << (M + 1)) as f64 * xm) as i32; // index in octave + 2^M
    let lij = je * (1 << M) + ip - loff; // index in lookup table
    let t = ((1 << (M + 2)) as f64 * xm) - (1 + 2 * ip) as f64;

    let p_idx = lij * 8;
    let q_idx = lij * 2;

    assert!(
        p_idx >= 0 && q_idx >= 0,
        "Negative indices not permitted in Rust."
    );

    // hard-coded for N1cheb=10
    ((((((((CHEBYSHEV_COEFFS_1[(p_idx + 7) as usize] * t
        + CHEBYSHEV_COEFFS_1[(p_idx + 6) as usize])
        * t
        + CHEBYSHEV_COEFFS_1[(p_idx + 5) as usize])
        * t
        + CHEBYSHEV_COEFFS_1[(p_idx + 4) as usize])
        * t
        + CHEBYSHEV_COEFFS_1[(p_idx + 3) as usize])
        * t
        + CHEBYSHEV_COEFFS_1[(p_idx + 2) as usize])
        * t
        + CHEBYSHEV_COEFFS_1[(p_idx + 1) as usize])
        * t
        + CHEBYSHEV_COEFFS_1[p_idx as usize])
        * t
        + CHEBYSHEV_COEFFS_0[(q_idx + 1) as usize])
        * t
        + CHEBYSHEV_COEFFS_0[q_idx as usize]
}

/// The scaled complementary error function, or erfcx(x).
///
/// ### Definition:
/// erfcx(x) = erfc(x) / exp(-x^2) = (1 - erf(x)) / exp(-x^2)
///
/// ### Input:
/// - x: A real number
///
/// ### Output:
/// The result of the computation, expected to match `libcerf` to at least 1e-15.
///
/// ### Notes on Implementation:
/// This is a Rust port of the erfcx routine found in [`libcerf`](https://jugit.fz-juelich.de/mlz/libcerf),
/// which is under the MIT License.
///
/// It uses the following methods:
/// - Asymptotic expansion for large positive x,
/// - Chebyshev polynomials for medium positive x,
/// - Maclaurin series for small |x|,
/// - Asymptote 2exp(x^2) for large negative x,
/// - 2*exp(x^2)-erfcx(-x) for medium negative x.
pub fn erfcx(x: f64) -> f64 {
    let ax = x.abs();

    if ax < 0.125 {
        /* **************************************************************** */
        /* Use the Maclaurin series.                                        */
        /* **************************************************************** */
        return (((((((((((((1.984_126_984_126_984e-4 * x - 5.344_009_079_373_427e-4)
            * x
            + 1.388_888_888_888_889e-3)
            * x
            - 3.473_605_901_592_727_4e-3)
            * x
            + 8.333_333_333_333_333e-3)
            * x
            - 1.910_483_245_876e-2)
            * x
            + 4.166_666_666_666_666_4e-2)
            * x
            - 8.597_174_606_442e-2)
            * x
            + 1.666_666_666_666_666_6e-1)
            * x
            - 3.009_011_112_254_700_3e-1)
            * x
            + 5e-1)
            * x
            - 7.522_527_780_636_751e-1)
            * x
            + 1.0)
            * x
            - FRAC_2_SQRT_PI)
            * x
            + 1.0;
    }

    if x < 0. {
        if x < -26.7 {
            return f64::INFINITY;
        }
        if x < -6.1 {
            return 2. * (x * x).exp();
        }
        return 2. * (x * x).exp() - cheb_interpolant(-x);
    }

    if x <= 12. {
        return cheb_interpolant(x);
    }

    /* **************************************************************** */
    /*     Use asymptotic expansion.                                    */
    /* Coefficients are a_0 = 1 / sqrt(PI),                             */
    /*                  a_N = (2N-1)!! / 2^N / sqrt(PI)                 */
    /* **************************************************************** */
    let r = 1. / x;

    if x < 150. {
        if x < 23.2 {
            return ((((((((((3.607_337_150_008_376e5 * (r * r)
                - 3.797_197_000_008_816_4e4)
                * (r * r)
                + 4.467_290_588_245_667e3)
                * (r * r)
                - 5.956_387_450_994_222e2)
                * (r * r)
                + 9.163_673_001_529_573e1)
                * (r * r)
                - 1.666_122_363_914_467_6e1)
                * (r * r)
                + 3.702_494_142_032_150_7)
                * (r * r)
                - 1.057_855_469_152_043)
                * (r * r)
                + 4.231_421_876_608_172_4e-1)
                * (r * r)
                - 2.820_947_917_738_781_4e-1)
                * (r * r)
                + 5.641_895_835_477_563e-1)
                * r;
        }
        return ((((((9.163_673_001_529_573e1 * (r * r) - 1.666_122_363_914_467_6e1) * (r * r)
            + 3.702_494_142_032_150_7)
            * (r * r)
            - 1.057_855_469_152_043)
            * (r * r)
            + 4.231_421_876_608_172_4e-1)
            * (r * r)
            - 2.820_947_917_738_781_4e-1)
            * (r * r)
            + 5.641_895_835_477_563e-1)
            * r;
    }
    if x < 6.9e7 {
        return ((((-1.057_855_469_152_043) * (r * r) + 4.231_421_876_608_172_4e-1) * (r * r)
            - 2.820_947_917_738_781_4e-1)
            * (r * r)
            + 5.641_895_835_477_563e-1)
            * r;
    }
    // 1-term expansion, important to avoid overflow
    INV_SQRT_PI / x
}

