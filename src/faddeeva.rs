//! This module provides an implementation of the complex Faddeeva function.

use core::f64::consts::FRAC_2_SQRT_PI;

use crate::{dawson, error::erfcx, INV_SQRT_PI};

/// The complex Faddeeva function, or w(z). This is Abramowitz
/// & Stegun function (7.1.3), also known as the plasma dispersion
/// function or Kramp function.
///
/// ### Definition:
/// w(z) = exp(-z^2) erfc(-iz)
///
/// ## Inputs:
/// - re: ℜ(z)
/// - im: ℑ(z)
///
/// ## Outputs:
/// A tuple of ([`f64`], [`f64`]) where the first
/// is ℜ(w(z)) and the second is ℑ(w(z)).
///
/// ### Notes on Implementation:
/// This is effectively a Rust port of `libcerf` and the
/// [Faddeeva package](http://ab-initio.mit.edu/faddeeva/)
/// written by Steven G. Johnson at MIT. (MIT License)
///
/// For sufficiently large |z|, it uses a continued-fraction expansion,
/// which is an independent reimplementation of W. Gautschi's algorithm
/// in [this paper](http://dx.doi.org/10.1137/0707012). It is roughly
/// similar to [Algorithm 680](http://dx.doi.org/10.1145/77626.77629).
///
/// For ℑ(z) near the real axis or small |z|, it uses an independent reimplementation
/// of M. Zaghloul's [Algorithm 916](http://dx.doi.org/10.1145/2049673.2049679).
pub fn faddeeva(re: f64, im: f64) -> (f64, f64) {
    use num_complex::Complex64 as c64;
    let mut res: c64;

    let z = c64::new(re, im);
    let x = re;
    let xabs = re.abs();
    let y = im;
    let yabs = im.abs();
    let z_sqared = xabs * xabs + y * y;

    /* **************************************************************** */
    /*     Case when |ℜ(z)| >> |ℑ(z)|: In this scenario we may have    */
    /* precision issues because the complex norm can be very accurate,  */
    /* but either of the components may be off by orders of magnitude.  */
    /* **************************************************************** */

    if yabs < 1e-8 * xabs {
        let wi = im_w_of_x(x);
        let e2 = if xabs > 27. {
            0.
        } else {
            f64::exp(-xabs * xabs)
        };
        if yabs == 0. {
            return (e2, wi);
        }
        return (e2 + y * 2. * (x * wi - INV_SQRT_PI), wi - 2. * x * y * e2);
    }

    /* **************************************************************** */
    /*     Case when |ℑ(z)| >> |ℜ(z)|: In this scenario we may have    */
    /* precision issues because the complex norm can be very accurate,  */
    /* but either of the components may be off by orders of magnitude.  */
    /* **************************************************************** */

    if xabs < 1e-8 * yabs {
        let wr = erfcx(y);
        if xabs == 0. {
            return (wr, 0.);
        }
        return (wr, x * (2. * (INV_SQRT_PI - y * wr)));
    }

    /* **************************************************************** */
    /*     Case when |z| -> 0: Use the MacLaurin series.                */
    /* **************************************************************** */

    if z_sqared < 0.053 {
        if z_sqared < 0.00689 {
            if z_sqared < 4e-7 {
                res = ((((
                              c64::new(5e-1, 0.) ) * z // z^4
                          + c64::new(0., -7.522_527_780_636_751e-1) ) * z // z^3
                         + c64::new(-1.0, 0.) ) * z // z^2
                        + c64::new(0., FRAC_2_SQRT_PI) ) * z // z^1
                    + 1.;

                return (res.re, res.im);
            }

            res = (((((((((((((
                                   c64::new(0., 5.344_009_079_373_427e-4) ) * z // z^13
                               + c64::new(1.388_888_888_888_889e-3, 0.) ) * z // z^12
                              + c64::new(0., -3.473_605_901_592_727_4e-3) ) * z // z^11
                             + c64::new(-8.333_333_333_333_333e-3, 0.) ) * z // z^10
                            + c64::new(0., 1.910_483_245_876e-2) ) * z // z^9
                           + c64::new(4.166_666_666_666_666_4e-2, 0.) ) * z // z^8
                          + c64::new(0., -8.597_174_606_442e-2) ) * z // z^7
                         + c64::new(-1.666_666_666_666_666_6e-1, 0.) ) * z // z^6
                        + c64::new(0., 3.009_011_112_254_700_3e-1) ) * z // z^5
                       + c64::new(5e-1, 0.) ) * z // z^4
                      + c64::new(0., -7.522_527_780_636_751e-1) ) * z // z^3
                     + c64::new(-1.0, 0.) ) * z // z^2
                    + c64::new(0., FRAC_2_SQRT_PI) ) * z // z^1
                + 1.;

            return (res.re, res.im);
        }

        res = (((((((((((((((((((
				     c64::new(0., -8.823_957_200_203_801e-7) ) * z // z^19
				 + c64::new(-2.755_731_922_398_589_3e-6, 0.) ) * z // z^18
				+ c64::new(0., 8.382_759_340_193_61e-6) ) * z // z^17
			       + c64::new(2.480_158_730_158_73e-5, 0.) ) * z // z^16
			      + c64::new(0., -7.125_345_439_164_569e-5) ) * z // z^15
			     + c64::new(-1.984_126_984_126_984e-4, 0.) ) * z // z^14
			    + c64::new(0., 5.344_009_079_373_427e-4) ) * z // z^13
			   + c64::new(1.388_888_888_888_889e-3, 0.) ) * z // z^12
			  + c64::new(0., -3.473_605_901_592_727_4e-3) ) * z // z^11
			 + c64::new(-8.333_333_333_333_333e-3, 0.) ) * z // z^10
			+ c64::new(0., 1.910_483_245_876e-2) ) * z // z^9
		       + c64::new(4.166_666_666_666_666_4e-2, 0.) ) * z // z^8
		      + c64::new(0., -8.597_174_606_442e-2) ) * z // z^7
		     + c64::new(-1.666_666_666_666_666_6e-1, 0.) ) * z // z^6
		    + c64::new(0., 3.009_011_112_254_700_3e-1) ) * z // z^5
		   + c64::new(5e-1, 0.) ) * z // z^4
		  + c64::new(0., -7.522_527_780_636_751e-1) ) * z // z^3
		 + c64::new(-1.0, 0.) ) * z // z^2
		+ c64::new(0., FRAC_2_SQRT_PI) ) * z // z^1
	    + 1.;

        return (res.re, res.im);
    }

    /* **************************************************************** */
    /*     Case when |z| -> infinity: Use the MacLaurin series.         */
    /* **************************************************************** */

    if z_sqared >= 49. {
        let xs = if y < 0. { -z.re } else { z.re }; // compute for -z if y < 0

        if z_sqared > 4.8e15 {
            // Scale to prevent overflow.
            if xabs > yabs {
                let yax = yabs / xs;
                let denom = INV_SQRT_PI / (xs + yax * yabs);
                res = c64::new(denom * yax, denom);
            } else if yabs.is_infinite() {
                res = if xabs.is_nan() || y < 0. {
                    c64::new(f64::NAN, f64::NAN)
                } else {
                    c64::new(0., 0.)
                };
                return (res.re, res.im);
            } else {
                let xya = xs / yabs;
                let denom = INV_SQRT_PI / (xya * xs + yabs);
                res = c64::new(denom, denom * xya);
            }
        } else {
            let zm2 = 1. / z_sqared; // 1/|z|^2
            let r = c64::new(yabs * zm2, xs * zm2); // i/z
            let zm4 = zm2 * zm2; // 1/|z|^4
            let r2 = c64::new(zm4 * (xs + yabs) * (xs - yabs), -2. * zm4 * xs * yabs); // 1/z^2

            if z_sqared > 540. {
                if z_sqared > 22500. {
                    res = (((1.057_855_469_152_043 * r2 // n=3
			     + 4.231_421_876_608_172_4e-1) * r2 // n=2
			    + 2.820_947_917_738_781_4e-1) * r2 // n=1
			   + 5.641_895_835_477_563e-1)
                        * r; // n=0
                } else {
                    res = (((((((((((3.787_704_007_508_795e6 * r2 // n=11
				     + 3.607_337_150_008_376e5) * r2 // n=10
				    + 3.797_197_000_008_816_4e4) * r2 // n=9
				   + 4.467_290_588_245_667e3) * r2 // n=8
				  + 5.956_387_450_994_222e2) * r2 // n=7
				 + 9.163_673_001_529_573e1) * r2 // n=6
				+ 1.666_122_363_914_467_6e1) * r2 // n=5
			       + 3.702_494_142_032_150_7) * r2 // n=4
			      + 1.057_855_469_152_043) * r2 // n=3
			     + 4.231_421_876_608_172_4e-1) * r2 // n=2
			    + 2.820_947_917_738_781_4e-1) * r2 // n=1
			   + 5.641_895_835_477_563e-1)
                        * r; // n=0
                }
            } else {
                res = (((((((((((((((((((8.824_926_094_302_537e15 * r2 // n=19
                                         + 4.770_230_321_244_615e14) * r2 // n=18
                                        + 2.725_845_897_854_065_6e13) * r2 // n=17
                                       + 1.652_027_816_881_252e12) * r2 // n=16
                                      + 1.065_824_397_987_904_4e11) * r2 // n=15
                                     + 7.350_513_089_571_754e9) * r2 // n=14
                                    + 5.444_824_510_793_892e8) * r2 // n=13
                                   + 4.355_859_608_635_114e7) * r2 // n=12
                                  + 3.787_704_007_508_795e6) * r2 // n=11
                                 + 3.607_337_150_008_376e5) * r2 // n=10
                                + 3.797_197_000_008_816_4e4) * r2 // n=9
                               + 4.467_290_588_245_667e3) * r2 // n=8
                              + 5.956_387_450_994_222e2) * r2 // n=7
                             + 9.163_673_001_529_573e1) * r2 // n=6
                            + 1.666_122_363_914_467_6e1) * r2 // n=5
                           + 3.702_494_142_032_150_7) * r2 // n=4
                          + 1.057_855_469_152_043) * r2 // n=3
                         + 4.231_421_876_608_172_4e-1) * r2 // n=2
                        + 2.820_947_917_738_781_4e-1) * r2 // n=1
                       + 5.641_895_835_477_563e-1)
                    * r; // n=0
            }
        }
        if y < 0. {
            // Use w(z) = 2.0*exp(-z*z) - w(-z),
            // but be careful of overflow in exp(-z*z) = exp(-(xs*xs-ya*ya) -2*i*xs*ya)
            res = 2.0 * c64::new((yabs - xs) * (xs + yabs), 2. * xs * y).exp() - res;
            return (res.re, res.im);
        } else {
            return (res.re, res.im);
        }
    }

    if xabs.is_nan() {
        return (xabs, xabs);
    }
    if y.is_nan() {
        return (y, y);
    }

    /* **************************************************************** */
    /*     Fall back to a Taylor series around z.                       */
    /* **************************************************************** */

    let i_tile =
        2 * ((INV_A * xabs) as isize * N_X_COVER as isize + (INV_A * yabs) as isize) as usize;
    let kp = TILES[i_tile];
    assert!(kp >= 0, "kP is always >= 0 in the libcerf code.");
    let idx = 2 * (NTAY + 1) * kp as usize;
    let t = TAYLOR_COEFFS[idx];
    let t_next = TAYLOR_COEFFS[idx + 1];
    let dz = c64::new(xabs - t, yabs - t_next);

    // Something is wrong here...
    let nk = TILES[i_tile + 1];
    res = c64::new(
        TAYLOR_COEFFS[(idx as isize + 2 * nk as isize) as usize],
        TAYLOR_COEFFS[(idx as isize + 2 * nk as isize) as usize + 1],
    );
    // Equivalent to `for (int k = Nk-1; k >= 1; --k)` in libcerf.
    for k in (1..nk).rev() {
        res = res * dz
            + c64::new(
                TAYLOR_COEFFS[(idx as isize + (2 * k as isize)) as usize],
                TAYLOR_COEFFS[(idx as isize + (2 * k as isize)) as usize + 1],
            );
    }

    if y < 0. {
        if x < 0. {
            res = 2.0 * c64::new((y - x) * (x + y), -2. * x * y).exp() - res;
            return (res.re, res.im);
        }
        res = 2. * (c64::new((y - x) * (x + y), -2. * x * y)).exp() - c64::new(res.re, -(res.im));
        return (res.re, res.im);
    }
    if x < 0. {
        res = c64::new(res.re, -res.im);
    }
    (res.re, res.im)
}

/// Computes the scaled Dawson integral of x,
/// which is 2 * Dawson(x) / sqrt(pi).
/// This equals the imaginary part of the Faddeeva
/// function of x, for real x.
fn im_w_of_x(x: f64) -> f64 {
    2. * dawson(x) * INV_SQRT_PI
}

