//! This test compares the Rust port's output with the output from
//! the following C program:
//!
//! ```C
//! #include <cerf.h>
//! #include <stdio.h>
//! #include <complex.h>
//!
//! int main() {
//!     double complex z = 0.0 + 0.0 * I;
//!     for (int i = 0; i < 1000; i++) {
//!             z += 0.01 + 0.01 * I;
//!         printf("(%.17lf, %.17lf),\n", creal(w_of_z(z)), cimag(w_of_z(z)));
//!     }
//! }
//! ```
//!
//! This can be compiled with the following command:
//! ```bash
//! gcc -o myprogram myprogram.c -lcerf -lm
//! ```

use puruspe::faddeeva::faddeeva;

const MAX_ERR: f64 = 1e-15;

#[test]
fn test_faddeeva() {
    let mut z = (0., 0.);
    for i in 0..1000 {
        z = (z.0 + 0.01, z.1 + 0.01);
        let w = faddeeva(z.0, z.1);
        let libcerf_val = W_OF_Z_TABLE[i];

        println!("RE(Z): {}, IM(Z): {}", z.0, z.1);
        println!("REAL DIFFERENCE: {}", (libcerf_val.0 - w.0).abs());
        println!("IMAG DIFFERENCE: {}", (libcerf_val.1 - w.1).abs());

        assert!((libcerf_val.0 - w.0).abs() < MAX_ERR);
        assert!((libcerf_val.1 - w.1).abs() < MAX_ERR);
    }
}

